    private void btnInserirListaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInserirListaActionPerformed
        String nome = txtNomeLista.getText();
        
        if(nome.isEmpty()){
            JOptionPane.showMessageDialog(null, "Campo vazio!");
        }else{
            Lista lst = new Lista();
            lst.setNomeLista(nome);
            lst.criarLista(lst);
            
            txtNomeLista.setText("");
            preencreTabela("SELECT * FROM Lista");
        }
    }//GEN-LAST:event_btnInserirListaActionPerformed

