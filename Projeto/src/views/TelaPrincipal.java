    public void preencreTabela(String sql){
        ArrayList dados = new ArrayList();
        String[] colunas = new String[]{"Nome da Lista"};
        conn = new ConexaoBD().getConnection();
        
        try {
            pstm = conn.prepareStatement(sql);
            rs = pstm.executeQuery();
            rs.first();
            do{
                dados.add(new Object[]{rs.getString("nomeLista")});
            }while(rs.next());
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Erro ao preencher tabela de listas. ERRO:" + e);
        }
        
        Tabela tbl =  new Tabela(dados, colunas);
        tblLista.setModel(tbl);
        
        TableRowSorter sorter = new TableRowSorter(tbl);
        tblLista.setRowSorter(sorter);
        
        tblLista.getColumnModel().getColumn(0).setPreferredWidth(185);
        tblLista.getColumnModel().getColumn(0).setResizable(false);
        tblLista.getTableHeader().setReorderingAllowed(false);
        tblLista.setAutoResizeMode(tblLista.AUTO_RESIZE_OFF);
        tblLista.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
    }
    
